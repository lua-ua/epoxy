// ефект для іконки INSTAGRAM
let contactIconScaleInstagram = document.getElementById('instagram-icon');
let contactVisibleInstagram = document.getElementById('instagram-text');

contactIconScaleInstagram.addEventListener('click', function() {
    contactIconScaleInstagram.classList.toggle('scale');
    contactVisibleInstagram.classList.toggle('visible');
});


